# CONDA
# Conda is an open-source, cross-platform package and environment management system
# Conda extends its capabilities to handle packages and dependencies across various programming languages 

# IMPORTANT CONDA COMMANDS

# conda create - new conda environment
# conda activate - activates a specific conda environment 
# conda deactivate - deactivates
# conda install - installs packages into the environment 
# conda list - lists all packages installed
# conda search - searches for available packages 
# conda remove - removes a package 
# conda env list - lists all Conda environments in the system

# Pip handles pure Python packages, Conda is advantageous for packages that are in other languages

# Pip connects to the Python Package Index and will locate the library
# you can install multiple packages in a single command 


