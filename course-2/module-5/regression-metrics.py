# REGRESSION METRICS 

# They act as your performance indicators, 
# shining a light on how well your model is doing its job.

# Mean squared error, or MSE.
# MSE calculates the average of the squared differences between your model's predictions and the actual values.
# it squares the errors - sensitive to outliers 
# but it can be helpful if you want to be sensitive to large errors 

# Mean absolute error or MAE
# calculates the average of the absolute differences between your predictions and the actual values.
# It treats all errors the same, so those errors won't throw off your model too much.

# R Squared - coefficient of determination
# tells you how much of the variation in your target variable is explained by your model.
# percentage score - telling how well your model is capturing the data (ranges from 0-1)
# a high R Squared score does not always mean the model is perfect 
# could be overfit with the training data 
# a low R Squared Score could mean that the model is underfit 


