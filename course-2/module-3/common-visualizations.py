# COMMON VISUALIZATIONS 

# Pie chart 
# Visual appeal of pie charts lie in their simplicity, 
# provide an immediate sense of dominance 
# have small categories 
# less effective for small data segments 

# Bar chart
# excellent for comparing data across different groups
# handle a range of data types 
# they can be too bulky with too many categories 

# Histograms
# distribution of numerical data 
# ideal for understanding frequency of data points 
# reveal underlying patterns, skewness, or outliers 
# sensitivity to bin width 

# Line charts
# display trends over time 
# financial analysis, inventory levels, website traffic trends
# can only be accurate in they're sparsed or irregularly spaced
# Overlapping lines can also make it difficult without careful color management

# Scatter plots 
# plot data points on a horizontal and vertical axis to show the relationship between two variables
# vast datasets may appear cluttered
# Additional analysis may be needed if overlapping

# Table visuals 
# they organize information into rows and columns 
# helpful when precision and detail is needed 
# they can become overwhelming if there's too large datasets
# ineffective to communicate trends 


