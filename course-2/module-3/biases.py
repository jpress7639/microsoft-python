# Common Biases

# Small sample sizes - don't accurately portray the entire population
# Make sure it's large enough and diverse

# Survivorship bias - survivors of a process, you miss out on crucial insights
# Ensure your analysis incorporates both successes and failures

# Sampling bias - is not representing the population you're trying to sample
# Consider how you attain your sample

# Confirmation bias - tendency to favor information to confirm pre-existing beliefs
# Actively seek out diverse perspectives

# Anchoring bias - first piece of information influences your judgement 
# be aware of its influence, consider information objectively 

# Echo chambers - only encouter information and opinions that align with your own 
# Actively seek out different viewpoints and actively seek out discussions with people who have different viewpoints 

# Other factors leading to bias:
# Outliers - data points that significantly differ from the rest of your data 
# removing them or using statistical methods that are less sensitive to outliers 

# Missing data 
# Replacing values with the average

# Correlation vs causation - one may not cause the other 
# Always be cautious about inferring causation with correlation 

# Data visualization 
# Misleading visualizations can distort the truth and lead to incorrect conclusions 

